#En python, existen distintas operaciones para manipular una cadena de caracteres (strings). 

#-La asignación- 
#consiste en asignar una cadena de caracteres a otra += son operadores

#-La concatenación-
#Es una operación que consiste en unir dos cadenas o más, para formar una cadena de mayor tamaño +

#-La búsqueda-
#Consiste en localizar dentro de una cadena, una subcadena más pequeña a un carácter

#-La extracción-
#Se trata de sacar fuera de una cadena, una porción de la misma según su posición dentro de ella [1:8]

