#-palabras reservadas-
#Son identificadores para uso exclusivo del lenguaje de programación que no pueden ser utilizadas para identificar y nombrear variables, métodos, objetos o cualquier elemento dentro de nuestro código

#En python, existe un total de 28 palabras reservadas, las cuales fueron asignadas para uso exclusivo del lenguaje de programación: 
#(and, del, for, is, raise, assert, if, else, elif, from, lambda, return, break, global, not, try, class, except, or, while, continue, exec, import, yield, def, finally, in, print) 
